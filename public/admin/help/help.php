	<tr>
		<td colspan="2" align="center">
			<b>Sheet Properties</b> allows you to modify the columns of each WebSheet.  You can add columns, edit existing columns, delete columns, delete the entire websheet, or create a new websheet.
			<p><img src="help-sheetprops-addcol.gif"></p>
			First is <b>Add Column</b>.  To add a column, first select where you would like to the column to appear in the websheet.  If you want the column to be the first new column, you do NOT have 
			to change the "Insert After" pulldown menu.  It will automatically insert it at the beginning of the websheet, otherwise, the column will be added AFTER the column you select from the drop down menu.
			Second, type a name for the column.  Select what type the column should be: Letters AND numbers (Mixed content like addresses: "29376 Matthewstown Rd."), just letters, just numbers, or for really long entries "paragraphs."  Paragraphs are good for columns 
			like "comments" or "descriptions" that may contain long sentences. Then, decide what size the column should be.  This controls the horizontal width of the column.  One letter or number equal one "size."  
			So if you're going to have a column of Zip Codes, the size should be AT LEAST five.  For things like names, be generous, but not extreme.  Consider how many letters a name usually has "Jeff Earls" is nine, 
			plus the space makes ten.  DON'T FORGET SPACES!!  You can always go back and change the properties of the column afterwards, so don't freak out about it.  <b>Note:</b> Paragraphs do NOT use a size.  Click the green plus to create the column.
			<p><img src="help-sheetprops-editcol.gif"></p>
			<b>Edit Column</b> is where you can make those changes.  Edit column is very similar to add column.  First select which column you want to change by selecting it from the drop down menu.  If you
			want to rename the column, type a name, otherwise, leave it blank.  If you want to change what type the column is, say, from letters to numbers, or letters and numbers to paragraphs, make that change
			with the drop down menu.  Finally, select what size, as above, you want the column to be.  <b>Note:</b> Paragraphs do NOT use a size.  Click the green plus to apply the changes.
			<p><img src="help-sheetprops-deletecol.gif"></p>
			<b>Delete Column</b> allows you to delete a specific column.  Select which column you want to delete, then click the delete button.  It will ask if you are sure you want to delete the column.  <b>BE CAREFUL WHAT YOU DELETE, IT CANNOT MAGICALLY BE UNDELETED.</b>
			<p><img src="help-sheetprops-createsheet.gif"></p>
			<b>Create Sheet</b> allows you to create a new WebSheet.  Simply type the name of the sheet you prefer and click the green plus to create the sheet.  You will then be taken directly to the new sheet you just created.
			<p><img src="help-sheetprops-deletesheet.gif"></p>
			<b>Delete Sheet</b> allows you to delete the sheet you are currently viewing.  Just click the delete button.  It will ask you if you are sure if you want to delete the sheet.  <b>BE CAREFUL WHAT YOU DELETE, IT CANNOT MAGICALLY BE UNDELETED.</b>
		</td>
