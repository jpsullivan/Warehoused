	<tr>
		<td colspan="2" align="center">
			<b>Report</b> allows you to create printable reports from the WebSheet you are currently viewing.  After clicking the report button, select which columns you want to create a report from
			by holding down the Control button (CTRL) and LEFT clicking on the column names.  Then, select where you want to select what you're selecting from.  For example, if you only want available
			units, click the checkbox next to "STATUS" then type an "A" in the textbox.  The pull down menu between the column name and the text box allows you to use operations to select certain things.
			"=" means equals, "!=" means not equals, ">" means greater than, ">=" means greater than or equal to, "<" means less than, "<=" means less than or equal to, and "LIKE" means similar... Based
			on what you search for, such as "hello" LIKE will find "hello" in a sentence like "My name is Jeff, hello" or "Hello, my name is Jeff."  You can then select how you would like your report ordered.
			Select which column you would like your results ordered by.  You may sort by up to three orders, starting from the top-most drop down box.  Finally, click the "Generate Report" button at the bottom
			to create your report.  Click the Print button after the report has loaded.
		</td>
	</tr>
